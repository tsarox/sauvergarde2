int			ft_atoi(char *str)
{
	int		i;
	int		negat;
	int		nb;

	i = 0;
	negat = 0;
	nb = 0;
	while ((str[i] == ' ') || (str[i] == '\f') || (str[i] == '\t') \
			|| (str[i]) == '\n' || (str[i] == '\r') || (str[i] == '\v'))
		i++;
	if (str[i] == '-')
		negat = 1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	if (negat == 1)
		return (-nb);
	else
		return (nb);
}
